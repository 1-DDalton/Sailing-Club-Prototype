                DefaultTableModel model = (DefaultTableModel)dutyTbl.getModel();   
                model.setRowCount(0);    
                //Create a 2 dimensional array with 3 elements
                Object rowData[] = new Object[3];  
                //Fill up the array with the the next row of data from the list
